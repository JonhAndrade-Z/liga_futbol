package com;

/**
 * Enum que representa las posiciones de los jugadores en el campo de futbol.
 */

public enum Posicion {

    /** Posicion de delantero. */
    delantero,

    /** Posicion de lateral. */
    lateral,

    /** Posicion de portero. */
    portero,

    /** Posicion de medio centro. */
    medioCentro,

    /** Posicion de central. */
    central;
}
